class Solution {
  public:
    int gameOfXor(int N , int A[]) {
        int sum=0;
       if(N&1){
           
         for(int i=0;i<N;i+=2){
             sum=sum^A[i];
         } 
         return sum;
       }
       
       return 0;
    }
};

